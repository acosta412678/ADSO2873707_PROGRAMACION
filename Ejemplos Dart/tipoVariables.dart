void main () {

// TIPOS DE VARIABLES
  /*
  * int: Tipo de dato entero
  * double: Tipo de dato decimal
  * double: Tipo de dato decimal
  * String: Tipo de dato cadena de texto
  * bool: Tipo de dato booleano (true o false)
  */
  int num1 = 4;// Declaraciòn variable tipo entero 
  num1 = 5; // Asignaciòn de valor a vble num1
  print(num1); // Imprimir vble num1
